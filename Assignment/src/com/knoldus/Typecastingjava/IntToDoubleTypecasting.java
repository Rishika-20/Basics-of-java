package com.knoldus.Typecastingjava;

public class IntToDoubleTypecasting {

	public static void main(String[] args) {

		double distance = 5;

		// Typecasting double into an integer variable.

		int IntValue = (int) 5;

		System.out.println(" the value of distance in double is: " + distance);
		System.out.println(" the value of distance in integer is: " + IntValue);

	}

}
