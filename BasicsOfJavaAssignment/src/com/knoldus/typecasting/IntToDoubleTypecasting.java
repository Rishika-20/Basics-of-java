package com.knoldus.typecasting;

public class IntToDoubleTypecasting {

	public static void main(String[] args) {

		double distance = 5 ;
		
	// Typecasting double into an integer variable.
		
		int IntValue = (int) 5 ;
		
		System.out.println ("The value of distance  before typecasting: "+distance);
		System.out.println ("The value of distance after typecasting into an int: "+IntValue);

	}

}
